# set union intersection and difference

setx = {'green','orange','blue'}
sety = {'orange','blue','red'}
print(setx)
print(sety)
print("the intersection of two sets are")
print(setx.intersection(sety))
print("the union of two sets are")
print(setx.union(sety))
print("the difference of two sets are")
print(setx.difference(sety))